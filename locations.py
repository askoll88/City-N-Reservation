class Location:
    """Класс локации"""

    def __init__(self, location_id: str):
        self.id = location_id
        self.data = LOCATIONS[location_id]

    @property
    def name(self) -> str:
        return self.data["name"]

    @property
    def description(self) -> str:
        return self.data["description"]

    @property
    def exits(self) -> dict:
        return self.data["exits"]

    def get_exit(self, direction: str) -> str | None:
        """Получить ID локации по направлению движения"""
        return self.exits.get(direction.lower())


def get_location(location_id: str) -> Location:
    """Получить объект локации по ID"""
    if location_id not in LOCATIONS:
        return None
    return Location(location_id)


def get_all_locations() -> list[str]:
    """Получить список всех локаций"""
    return list(LOCATIONS.keys())
