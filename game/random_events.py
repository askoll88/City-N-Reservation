def _is_quest_available(user_id: int, chain_key: str) -> bool:
    """Проверить, доступна ли квестовая цепочка (не на кулдауне и не завершена)."""
    import time
    stage = _get_quest_stage(user_id, chain_key)
    chain_data = QUEST_CHAINS[chain_key]
    total_stages = len(chain_data["stages"])

    # Если квест завершён (стадия >= total_stages), проверяем кулдаун
    if stage >= total_stages:
        cooldown_until = _get_quest_cooldown(user_id, chain_key)
        now = int(time.time() / 3600)  # Текущий час
        if now < cooldown_until:
            return False  # На кулдауне
        else:
            # Кулдаун истёк — сбросить квест
            _set_quest_stage(user_id, chain_key, 0)
            _set_quest_cooldown(user_id, chain_key, 0)
            return True

    return True  # Квест в прогрессе или не начат


def _get_next_quest_event(user_id: int, corridor_id: str | None = None) -> dict | None:
    """
    Получить следующее событие из активной квестовой цепочки.
    Если игрок в середине квеста — вернуть нужный этап.
    """
    import random
    import time

    for chain_key, chain_data in QUEST_CHAINS.items():
        if not _is_quest_available(user_id, chain_key):
            continue

        stage = _get_quest_stage(user_id, chain_key)
        total_stages = len(chain_data["stages"])

        # Квест не начат — случайный шанс начать
        if stage == 0:
            # 15% шанс начать квест при каждом вызове
            if random.randint(1, 100) > 15:
                continue

        if stage < total_stages:
            # Получить события текущей стадии
            stage_event_ids = chain_data["stages"][stage]
            # Найти событие в RANDOM_EVENTS
            for event in RANDOM_EVENTS:
                if event["id"] in stage_event_ids and _event_weight_for_corridor(event, corridor_id) > 0:
                    return copy.deepcopy(event)

    return None


def _mark_quest_complete(user_id: int, chain_key: str):
    """Отметить квест как завершённый и установить кулдаун."""
    import time
    chain_data = QUEST_CHAINS[chain_key]
    total_stages = len(chain_data["stages"])
    cooldown_hours = chain_data["cooldown_hours"]

    _set_quest_stage(user_id, chain_key, total_stages)
    cooldown_until = int(time.time() / 3600) + cooldown_hours
    _set_quest_cooldown(user_id, chain_key, cooldown_until)


