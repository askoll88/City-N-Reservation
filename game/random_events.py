def _add_player_xp(player, amount: int) -> int:
    """Начислить XP игроку с учётом ограничений ранга."""
    gain = int(amount or 0)
    if gain <= 0:
        return 0
    add_fn = getattr(player, "add_experience", None)
    if callable(add_fn):
        try:
            return int(add_fn(gain) or 0)
        except Exception:
            pass
    player.experience += gain
    return gain


def _apply_hp_delta(player, delta: int) -> int:
    """Изменить HP: урон может добить до 0, лечение ограничено max_health."""
    current_hp = int(getattr(player, "health", 0) or 0)
    max_hp = int(getattr(player, "max_health", max(current_hp, 1)) or max(current_hp, 1))
    raw = current_hp + int(delta or 0)
    if delta < 0:
        player.health = max(0, raw)
    else:
        player.health = min(max_hp, raw)
    return int(player.health)


def apply_event_choice(event: dict, choice_index: int, player, user_id: int = None, stage_index: int = 0) -> dict:
    """
    Применить выбор игрока к событию.
    Возвращает dict:
      - message: текст результата
      - next_stage: индекс следующей стадии (None если событие завершено)
      - is_final: True если это последний этап
    Если передан user_id — продвигает квестовые цепочки.
    """
    if choice_index < 0:
        return {"message": "Ты не успел принять решение. Дорога не любит пустых жестов.", "next_stage": None, "invalid": True}

    # Многоэтапное событие
    if event.get("type") == "multi_stage":
        stages = event.get("stages", [])
        if stage_index >= len(stages):
            stage_index = len(stages) - 1

        stage = stages[stage_index]
        choices = stage.get("choices", [])

        if choice_index >= len(choices):
            return {"message": "Такого хода здесь нет. Зона ждёт конкретного решения.", "next_stage": None, "invalid": True}

        choice = choices[choice_index]

        # Применяем немедленные эффекты выбора
        if "effect" in choice:
            eff = choice["effect"]
            if "xp" in eff:
                _add_player_xp(player, eff["xp"])
            if "money" in eff:
                player.money += eff.get("money", 0)
            if "energy" in eff:
                player.energy = max(0, player.energy + eff["energy"])
            if "hp" in eff:
                _apply_hp_delta(player, int(eff["hp"]))
            if eff.get("random_loot"):
                _apply_random_loot(player)
            if eff.get("random_artifact"):
                _apply_random_artifact(player)
            if eff.get("artifact_chance"):
                _apply_artifact_chance(player)
            if eff.get("risk_combat"):
                _apply_risk_combat(player)
            if eff.get("risk_damage"):
                _apply_risk_damage(player)

        # Проверяем, последний ли это этап
        if choice.get("is_final") or stage_index == len(stages) - 1:
            # Применяем финальные эффекты
            final_effect = stage.get("final_effect", {})
            if "xp" in final_effect:
                _add_player_xp(player, final_effect["xp"])
            if "money" in final_effect:
                player.money += final_effect.get("money", 0)
            if "energy" in final_effect:
                player.energy = max(0, player.energy + final_effect["energy"])
            if "hp" in final_effect:
                _apply_hp_delta(player, int(final_effect["hp"]))

            # Квестовая цепочка
            _check_quest_progression(event, user_id)

            return {"message": stage["text"], "next_stage": None, "is_final": True}
        else:
            next_stage = choice.get("next_stage", stage_index + 1)
            return {"message": stage["text"], "next_stage": next_stage, "is_final": False}

    # Обычное событие
    if choice_index >= len(event.get("choices", [])):
        return {"message": "Такого хода здесь нет. Зона ждёт конкретного решения.", "next_stage": None, "invalid": True}

    choice = event["choices"][choice_index]
    effect = choice.get("effect", {})
    player_id = _resolve_player_id(player)

    # Квестовая цепочка
    _check_quest_progression(event, user_id)

    # Простое сообщение
    if "message" in effect and not any(k in effect for k in ["risk_damage", "risk_combat", "random_loot", "artifact_chance", "random_artifact", "random_dialog", "need_item", "shop_discount", "money_loss"]):
        if "xp" in effect:
            _add_player_xp(player, effect["xp"])
        if "money" in effect:
            player.money += effect["money"]
        if "energy" in effect:
            player.energy = max(0, player.energy + effect["energy"])
        if "shells" in effect:
            shell_gain = int(effect["shells"] or 0)
            if shell_gain > 0 and player_id is not None:
                from infra import database
                before_shells = int(database.get_user_shells(player_id) or 0)
                database.add_shells(player_id, shell_gain)
                after_shells = int(database.get_user_shells(player_id) or 0)
                actual_shell_gain = max(0, after_shells - before_shells)
                from handlers.quests import track_quest_shells
                if actual_shell_gain > 0:
                    track_quest_shells(player_id, count=actual_shell_gain)
        return {"message": effect["message"], "next_stage": None, "is_final": True}

    # Риск - получение урона
    if effect.get("risk_damage"):
        msg = _apply_risk_damage(player, effect)
        return {"message": msg, "next_stage": None, "is_final": True}

    # Риск - бой
    if effect.get("risk_combat"):
        msg = _apply_risk_combat(player, effect)
        return {"message": msg, "next_stage": None, "is_final": True}

    # Случайный лут
    if effect.get("random_loot"):
        loot_data = _apply_random_loot(player)
        if "money" in effect:
            player.money += effect["money"]
            loot_data["money_reward"] += effect["money"]
        msg_tpl = effect.get("message", "В схроне нашлись припасы: +{money} руб., +{item}")
        return {
            "message": _render_event_message(msg_tpl, loot_data),
            "next_stage": None,
            "is_final": True,
        }

    # Шанс артефакта
    if effect.get("artifact_chance"):
        msg = _apply_artifact_chance(player, effect)
        return {"message": msg, "next_stage": None, "is_final": True}

    # Случайный артефакт
    if effect.get("random_artifact"):
        msg = _apply_random_artifact(player)
        return {"message": msg or effect.get("message", "Артефактный след оказался настоящим."), "next_stage": None, "is_final": True}

    # Случайный диалог
    if effect.get("random_dialog"):
        dialog_xp = int(effect.get("xp", 30))
        gained_xp = _add_player_xp(player, dialog_xp)
        msg_tpl = effect.get("message", "Разговор в Зоне не прошёл зря.")
        return {
            "message": _render_event_message(msg_tpl, {"xp": gained_xp}),
            "next_stage": None,
            "is_final": True,
        }

    # Нужен предмет
    if effect.get("need_item"):
        msg = _apply_need_item(player, effect)
        return {"message": msg, "next_stage": None, "is_final": True}

    # Скидка
    if effect.get("shop_discount"):
        _add_player_xp(player, effect.get("xp", 0))
        return {"message": effect["message"], "next_stage": None, "is_final": True}

    # Потеря денег
    if effect.get("money_loss"):
        loss = effect["money_loss"]
        player.money = max(0, player.money - loss)
        return {"message": effect["message"], "next_stage": None, "is_final": True}

    # XP + money
    if "xp" in effect:
        _add_player_xp(player, effect["xp"])
    if "money" in effect:
        player.money += effect["money"]
    if "energy" in effect:
        player.energy = max(0, player.energy + effect["energy"])

    return {"message": effect.get("message", "Дорога проглотила событие без следа. Ты идёшь дальше."), "next_stage": None, "is_final": True}


# --- Вспомогательные функции для apply_event_choice ---

def _resolve_player_id(player) -> int | None:
    """Получить VK ID игрока из актуального или legacy-атрибута."""
    uid = getattr(player, "user_id", None)
    if uid is None:
        uid = getattr(player, "vk_id", None)
    try:
        return int(uid) if uid is not None else None
    except (TypeError, ValueError):
        return None

def _apply_random_loot(player):
    from infra import database
    player_id = _resolve_player_id(player)
    money_reward = random.randint(50, 300)
    player.money += money_reward
    item_text = "ничего"
    if random.randint(1, 100) <= 40:
        common_items = [("Бинт", 2), ("Аптечка", 1), ("Гильзы", 10), ("Хлеб", 1), ("Вода", 1)]
        item_name, qty = random.choice(common_items)
        if player_id is not None:
            database.add_item_to_inventory(player_id, item_name, qty)
            item_text = f"{item_name} x{qty}"
    return {
        "money_reward": money_reward,
        "item_text": item_text,
        "money": money_reward,
        "item": item_text,
    }


def _apply_random_artifact(player):
    from game.anomalies import get_artifact_from_anomaly
    from infra import database
    player_id = _resolve_player_id(player)
    anomaly_type = random.choice(["жарка", "электра", "воронка", "туман", "магнит"])
    artifact = get_artifact_from_anomaly(anomaly_type)
    if artifact and player_id is not None:
        database.add_item_to_inventory(player_id, artifact, 1)
        from handlers.quests import track_quest_artifact
        track_quest_artifact(player_id)
        return f"Детектор вывел тебя к артефакту: {artifact}."
    return "Артефактный след рассыпался прямо в руках, оставив только тепло на перчатках."


def _apply_artifact_chance(player, effect=None):
    from game.anomalies import get_random_anomaly, get_artifact_from_anomaly
    from infra import database
    from game.emission import get_emission_artifact_bonus
    player_id = _resolve_player_id(player)
    anomaly = get_random_anomaly()
    artifact = get_artifact_from_anomaly(anomaly["type"])
    success_chance = min(95, int(30 * get_emission_artifact_bonus()))
    if random.randint(1, 100) <= success_chance:
        if artifact and player_id is not None:
            database.add_item_to_inventory(player_id, artifact, 1)
            from handlers.quests import track_quest_artifact
            track_quest_artifact(player_id)
        msg_tpl = effect.get("message_ok", f"В аномальном следе проявился {artifact or 'артефакт'}.") if effect else f"В аномальном следе проявился {artifact or 'артефакт'}."
        return _render_event_message(msg_tpl, {"artifact": artifact or "артефакт"})
    else:
        _apply_hp_delta(player, -25)
        msg_tpl = effect.get("message_fail", "Аномальный фон сорвался раньше, чем ты успел отойти.") if effect else "Аномальный фон сорвался раньше, чем ты успел отойти."
        return _render_event_message(msg_tpl, {"artifact": artifact or "артефакт"})


def _apply_risk_combat(player, effect=None):
    if random.randint(1, 100) <= 60:
        money_gain = 0
        if effect and "money" in effect:
            money_gain = int(effect.get("money", 0))
            player.money += money_gain
        msg_tpl = effect.get("message_ok", "Ты вышел из ситуации целым и забрал то, что успел.") if effect else "Ты вышел из ситуации целым и забрал то, что успел."
        return _render_event_message(msg_tpl, {"money": money_gain})
    else:
        _apply_hp_delta(player, -30)
        msg_tpl = effect.get("message_fail", "Ситуация сорвалась: дорога ударила первой.") if effect else "Ситуация сорвалась: дорога ударила первой."
        return _render_event_message(msg_tpl, {})


def _apply_risk_damage(player, effect=None):
    if random.randint(1, 100) <= 40:
        _apply_hp_delta(player, -20)
        player.energy = max(0, player.energy - 15)
        msg_tpl = effect.get("message_fail", "Зона забрала плату за лишний риск.") if effect else "Зона забрала плату за лишний риск."
        return _render_event_message(msg_tpl, {})
    else:
        msg_tpl = effect.get("message_ok", "В этот раз маршрут отпустил тебя без серьёзной цены.") if effect else "В этот раз маршрут отпустил тебя без серьёзной цены."
        return _render_event_message(msg_tpl, {})


def _render_event_message(template: str, ctx: dict) -> str:
    """Безопасно подставить плейсхолдеры в тексте ивента."""
    if not template:
        return ""
    try:
        return template.format(
            money=ctx.get("money", ctx.get("money_reward", 0)),
            item=ctx.get("item", ctx.get("item_text", "ничего")),
            artifact=ctx.get("artifact", "артефакт"),
            xp=ctx.get("xp", 0),
        )
    except Exception:
        return template


def _apply_need_item(player, effect):
    player_id = _resolve_player_id(player)
    item_name = effect["need_item"]
    player.inventory.reload()
    has_item = any(
        item["name"].lower() == item_name.lower()
        for cat in [player.inventory.other, player.inventory.artifacts]
        for item in cat
    )
    if has_item and player_id is not None:
        from infra import database
        database.remove_item_from_inventory(player_id, item_name, 1)
        _add_player_xp(player, effect.get("xp", 0))
        return effect.get("message", f"Ты использовал {item_name}.")
    else:
        return f"У тебя нет {item_name}. Ты не можешь помочь."


def _check_quest_progression(event, user_id):
    """Проверить и продвинуть квестовую цепочку."""
    if user_id is None:
        return
    quest_info = _EVENT_TO_QUEST.get(event.get("id"))
    if not quest_info:
        return
    chain_key, stage_idx = quest_info
    chain_data = QUEST_CHAINS[chain_key]
    total_stages = len(chain_data["stages"])
    if stage_idx == total_stages - 1:
        _mark_quest_complete(user_id, chain_key)
    else:
        _set_quest_stage(user_id, chain_key, stage_idx + 1)
