class NPC:
    """Класс NPC"""

    def __init__(self, npc_id: str):
        self.id = npc_id
        self.data = NPCS[npc_id]

    @property
    def name(self) -> str:
        return self.data["name"]

    @property
    def description(self) -> str:
        return self.data["description"]

    @property
    def location(self) -> str:
        return self.data["location"]

    @property
    def greeting(self) -> str:
        return self.data["greeting"]

    def get_dialog(self, dialog_id: str) -> dict | None:
        """Получить диалог по ID"""
        return self.data["dialogs"].get(dialog_id)

    def get_menu(self) -> list:
        """Получить список доступных вопросов"""
        return self.data.get("menu", [])

    def get_dialog_text(self, dialog_id: str) -> str:
        """Получить текст ответа NPC"""
        dialog = self.get_dialog(dialog_id)
        if dialog:
            return dialog.get("answer", "NPC молчит.")
        return "NPC молчит."

    def get_question_text(self, dialog_id: str) -> str | None:
        """Получить текст вопроса для кнопки"""
        dialog = self.get_dialog(dialog_id)
        if dialog:
            return dialog.get("question")
        return None


def get_npc(npc_id: str) -> NPC | None:
    """Получить NPC по ID"""
    if npc_id not in NPCS:
        return None
    return NPC(npc_id)


def get_npc_by_location(location_id: str) -> list[NPC]:
    """Получить всех NPC в указанной локации"""
    result = []
    for npc_id in NPCS:
        npc = NPC(npc_id)
        if npc.location == location_id:
            result.append(npc)
    return result


def get_all_npcs() -> dict:
    """Получить всех NPC"""
    return NPCS
